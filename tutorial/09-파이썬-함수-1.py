# ============================================================================
# 함수를 매개변수로 전달
# ============================================================================

def apply_operation(x, y, operation):
    """연산을 적용하는 함수"""
    return operation(x, y)

def add(a, b):
    return a + b

def multiply(a, b):
    return a * b

result1 = apply_operation(3, 4, add)
#print(result1) # 7

result2 = apply_operation(3, 4, multiply)
#print(result2) # 12

# ============================================================================
# 함수를 반환값으로 사용
# ============================================================================

def create_multiplier(n):
    """배수를 만드는 함수를 반환"""
    def multiplier(x):
        return x * n
    return multiplier

double = create_multiplier(2)
triple = create_multiplier(3)

#print(double(5)) # 10
#print(triple(5)) # 15

