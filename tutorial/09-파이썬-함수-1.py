# 함수의 설명을 작성하는 데 사용된다.

def calculate_area(radius):
    """
    원의 넓이를 계산하는 함수
    
    매개변수:
        radius (float): 원의 반지름
    
    반환값:
        float: 원의 넓이
    """
    import math
    return math.pi * radius ** 2

