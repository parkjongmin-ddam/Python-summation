# 가변 인자 (*args)
# ============================================================================
# 임의의 개수의 위치 인자를 받을 수 있다.

def sum_numbers(*args):
    """여러 수의 합을 구하는 함수"""
    total = 0
    for num in args:
        total += num
    return total

result = sum_numbers(1, 2, 3, 4, 5)
#print(result) # 15

