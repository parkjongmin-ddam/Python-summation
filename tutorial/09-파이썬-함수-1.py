# 3. 매개변수에 기본값을 사용할 때는 가변 객체(리스트, 딕셔너리)를 사용하지 않는 것이 좋다.
# 4. docstring을 작성하여 함수의 목적과 사용법을 명확히 하는 것이 좋다.
