# 반복문 (for, while)
# 반복문은 특정 코드 블록을 여러 번 실행하는 데 사용된다.

# ============================================================================
# for 루프
# ============================================================================
# for 루프는 시퀀스(리스트, 튜플, 문자열 등)의 각 요소를 순회한다.

# 기본 for 루프
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    #print(fruit) # apple, banana, cherry를 순차적으로 출력
    pass

# range() 함수와 함께 사용
for i in range(5):
    #print(i) # 0, 1, 2, 3, 4를 순차적으로 출력
    pass

# range(start, stop, step)
for i in range(1, 10, 2):
    #print(i) # 1, 3, 5, 7, 9를 순차적으로 출력
    pass

# 문자열 순회
text = "Python"

for char in text:
    #print(char) # P, y, t, h, o, n을 순차적으로 출력
    pass

# ============================================================================
# enumerate() 함수와 함께 사용
# ============================================================================
# 인덱스와 값을 동시에 가져올 수 있다.

fruits = ["apple", "banana", "cherry"]

for index, fruit in enumerate(fruits):
    #print(f"{index}: {fruit}") # 0: apple, 1: banana, 2: cherry
    pass

# 시작 인덱스 지정
for index, fruit in enumerate(fruits, start=1):
    #print(f"{index}: {fruit}") # 1: apple, 2: banana, 3: cherry
    pass

# ============================================================================
# zip() 함수와 함께 사용
# ============================================================================
# 여러 시퀀스를 동시에 순회할 수 있다.

names = ["Alice", "Bob", "Charlie"]
ages = [25, 30, 35]

for name, age in zip(names, ages):
    #print(f"{name} is {age} years old")
    pass

# ============================================================================
# 중첩 for 루프
# ============================================================================

for i in range(3):
    for j in range(3):
        #print(f"({i}, {j})") # (0,0), (0,1), (0,2), (1,0), ...
        pass

# ============================================================================
# 리스트 컴프리헨션과 for 루프
# ============================================================================

# 리스트 컴프리헨션 (간결한 방법)
squares = [x**2 for x in range(5)]
#print(squares) # [0, 1, 4, 9, 16]

# 일반 for 루프 (동일한 결과)
squares = []
for x in range(5):
    squares.append(x**2)
#print(squares) # [0, 1, 4, 9, 16]

# ============================================================================
# while 루프
# ============================================================================
# while 루프는 조건이 True인 동안 코드 블록을 반복 실행한다.

# 기본 while 루프
count = 0

while count < 5:
    #print(count) # 0, 1, 2, 3, 4를 순차적으로 출력
    count += 1

# ============================================================================
# break 문
# ============================================================================
# 루프를 즉시 종료한다.

for i in range(10):
    if i == 5:
        break # i가 5가 되면 루프 종료
    #print(i) # 0, 1, 2, 3, 4만 출력

# ============================================================================
# continue 문
# ============================================================================
# 현재 반복을 건너뛰고 다음 반복으로 넘어간다.

for i in range(10):
    if i % 2 == 0:
        continue # 짝수면 건너뛰기
    #print(i) # 1, 3, 5, 7, 9만 출력

# ============================================================================
# else 절과 함께 사용
# ============================================================================
# for 루프나 while 루프에 else 절을 추가할 수 있다.
# else 절은 루프가 정상적으로 완료되었을 때 실행된다 (break로 종료되지 않았을 때).

for i in range(5):
    #print(i)
    pass
else:
    #print("루프가 정상적으로 완료되었습니다.") # break 없이 완료되면 실행
    pass

# break로 종료되면 else 절이 실행되지 않음
for i in range(5):
    if i == 3:
        break
    #print(i)
else:
    #print("이 메시지는 출력되지 않습니다.")
    pass

