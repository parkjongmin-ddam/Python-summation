# ============================================================================
# 반복문 제어 비교표
# ============================================================================
# | 제어문      | 설명                          | 사용 예제                |
# |------------|------------------------------|------------------------|
# | break      | 루프 즉시 종료                 | for i in range(10): if i == 5: break |
# | continue   | 현재 반복 건너뛰기              | for i in range(10): if i % 2 == 0: continue |
# | pass       | 아무것도 하지 않음              | for i in range(10): pass |
# | else       | 루프 정상 완료 시 실행          | for i in range(5): ... else: ... |
# ============================================================================

