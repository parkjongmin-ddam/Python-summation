# 패키지 구조
# 패키지는 여러 모듈을 묶어놓은 디렉토리이다.

# ============================================================================
# 패키지 구조 예제
# ============================================================================
# my_package/
#     __init__.py
#     module1.py
#     module2.py
#     subpackage/
#         __init__.py
#         module3.py

# ============================================================================
# 패키지 import
# ============================================================================

# 방법 1: import 패키지.모듈
# import my_package.module1
# my_package.module1.function()

# 방법 2: from 패키지 import 모듈
# from my_package import module1
# module1.function()

# 방법 3: from 패키지.모듈 import 함수
# from my_package.module1 import function
# function()

# 방법 4: from 패키지.서브패키지 import 모듈
# from my_package.subpackage import module3
# module3.function()

# ============================================================================
# __init__.py 파일
# ============================================================================
# __init__.py 파일은 패키지임을 나타내는 파일이다.
# Python 3.3+ 에서는 선택사항이지만, 하위 호환성을 위해 포함하는 것이 좋다.

# __init__.py 예제:
# from .module1 import function1
# from .module2 import function2

# ============================================================================
# 상대 import와 절대 import
# ============================================================================

# 절대 import (권장)
# from my_package.module1 import function

# 상대 import
# from .module1 import function  # 같은 패키지 내
# from ..parent_package.module import function  # 상위 패키지

# ============================================================================
# 주의사항
# ============================================================================
# 1. 패키지 이름은 소문자와 언더스코어를 사용하는 것이 관례이다.
# 2. __init__.py 파일을 사용하여 패키지를 초기화할 수 있다.
# 3. 절대 import를 사용하는 것이 권장된다.
# ============================================================================
