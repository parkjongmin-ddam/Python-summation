# 모듈 import
# 모듈은 Python 코드를 재사용하기 위해 만든 파일이다.

# ============================================================================
# 모듈 import 방법
# ============================================================================

# 방법 1: import 모듈명
import math
result = math.sqrt(16)
#print(result) # 4.0

# 방법 2: from 모듈명 import 함수명
from math import sqrt
result = sqrt(16)
#print(result) # 4.0

# 방법 3: from 모듈명 import *
from math import *
result = sqrt(16)
#print(result) # 4.0

# 방법 4: import 모듈명 as 별칭
import math as m
result = m.sqrt(16)
#print(result) # 4.0

