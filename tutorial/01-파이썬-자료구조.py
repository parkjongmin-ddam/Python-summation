# 숫자형(Number) 자료형
# 숫자형은 숫자 형태로 이루어진 자료형으로, 정수, 실수 등을 표현할 수 있다.
# Python에서는 정수형(int), 실수형(float), 복소수형(complex)을 지원한다.
# 숫자형은 불변성(Immutable)을 가지며, 한 번 생성된 숫자는 변경할 수 없다.

# 정수형(int) : 양의 정수, 음의 정수, 0을 표현할 수 있다.
a = 123
#print(a) # 123
#print(type(a)) # <class 'int'>

# 음의 정수
b = -456
#print(b) # -456
#print(type(b)) # <class 'int'>

# 0도 정수형이다.
c = 0
#print(c) # 0
#print(type(c)) # <class 'int'>

# 실수형(float) : 소수점이 있는 숫자를 표현할 수 있다.
d = 123.45
#print(d) # 123.45
#print(type(d)) # <class 'float'>

# 음의 실수
e = -3.14
#print(e) # -3.14
#print(type(e)) # <class 'float'>

# 과학적 표기법(Scientific Notation)을 사용할 수 있다.
f = 1.23e4 # 1.23 × 10^4 = 12300.0
#print(f) # 12300.0

g = 1.23e-4 # 1.23 × 10^-4 = 0.000123
#print(g) # 0.000123

# ============================================================================
# 진법 표현 방법 정리표
# ============================================================================
# | 진법    | 접두사      | 예제        | 10진수 값 | 설명                    |
# |--------|------------|------------|----------|------------------------|
# | 2진수  | 0b 또는 0B  | 0b1010     | 10       | 2진수 표현              |
# | 8진수  | 0o 또는 0O  | 0o177      | 127      | 8진수 표현              |
# | 16진수 | 0x 또는 0X  | 0x8F       | 143      | 16진수 표현 (A-F 사용)   |
# |        |            | 0XFF       | 255      | 대문자 접두사도 가능     |
# ============================================================================

# 2진수(Binary) : 0b 또는 0B 접두사를 사용하여 2진수를 표현할 수 있다.
binary_num = 0b1010 # 2진수 1010은 10진수로 10이다.
#print(binary_num) # 10
#print(type(binary_num)) # <class 'int'>

# 8진수(Octal) : 0o 또는 0O 접두사를 사용하여 8진수를 표현할 수 있다.
octal_num = 0o177 # 8진수 177은 10진수로 127이다.
#print(octal_num) # 127
#print(type(octal_num)) # <class 'int'>

# 16진수(Hexadecimal) : 0x 또는 0X 접두사를 사용하여 16진수를 표현할 수 있다.
hex_num = 0x8F # 16진수 8F는 10진수로 143이다.
#print(hex_num) # 143
#print(type(hex_num)) # <class 'int'>

# 대문자 접두사도 사용 가능하다.
hex_num2 = 0XFF # 16진수 FF는 10진수로 255이다.
#print(hex_num2) # 255

# 사칙연산 및 산술 연산자
# ============================================================================
# 산술 연산자 정리표
# ============================================================================
# | 연산자 | 의미          | 예제      | 결과        | 반환 타입    | 설명                    |
# |--------|--------------|----------|------------|------------|------------------------|
# | +      | 덧셈          | 10 + 5   | 15         | int/float  | 두 숫자를 더한다        |
# |--------|--------------|----------|------------|------------|------------------------|
# | -      | 뺄셈          | 10 - 5   | 5          | int/float  | 두 숫자를 뺀다          |
# |--------|--------------|----------|------------|------------|------------------------|
# | *      | 곱셈          | 10 * 5   | 50         | int/float  | 두 숫자를 곱한다        |
# |--------|--------------|----------|------------|------------|------------------------|
# | /      | 나눗셈        | 10 / 3   | 3.333...   | float      | 두 숫자를 나눈다 (항상 실수) |
# |--------|--------------|----------|------------|------------|------------------------|
# | //     | 정수 나눗셈   | 14 // 3  | 4          | int        | 나눗셈의 몫을 반환한다  |
# |--------|--------------|----------|------------|------------|------------------------|
# | %      | 나머지 연산   | 14 % 3   | 2          | int        | 나눗셈의 나머지를 반환한다 |
# |--------|--------------|----------|------------|------------|------------------------|
# | **     | 거듭제곱      | 2 ** 3   | 8          | int/float  | 첫 번째 숫자를 두 번째 숫자만큼 거듭제곱 |
# |        |              | 10 ** 2  | 100        |            |                        |
# ============================================================================
# 연산 우선순위 (높은 순서부터)
# ============================================================================
# | 우선순위 | 연산자      | 설명                    | 예제        | 결과  |
# |---------|------------|------------------------|------------|------|
# | 1       | ()         | 괄호 (가장 높은 우선순위) | (2 + 3) * 4 | 20   |
# | 2       | **         | 거듭제곱                 | 2 ** 3     | 8    |
# | 3       | *, /, //, %| 곱셈, 나눗셈, 나머지     | 2 + 3 * 4  | 14   |
# | 4       | +, -       | 덧셈, 뺄셈 (가장 낮은 우선순위) | 2 + 3 - 1 | 4 |
# ============================================================================
# 주의: 같은 우선순위의 연산자는 왼쪽에서 오른쪽으로 계산된다.

# 덧셈(+) : 두 숫자를 더한다.
result1 = 10 + 5
#print(result1) # 15

# 뺄셈(-) : 두 숫자를 뺀다.
result2 = 10 - 5
#print(result2) # 5

# 곱셈(*) : 두 숫자를 곱한다.
result3 = 10 * 5
#print(result3) # 50

# 나눗셈(/) : 두 숫자를 나눈다. 결과는 항상 실수형(float)이다.
result4 = 10 / 3
#print(result4) # 3.3333333333333335
#print(type(result4)) # <class 'float'>

# 정수 나눗셈(//) : 두 숫자를 나누고 몫을 반환한다. 결과는 정수형(int)이다.
result5 = 14 // 3 # 14를 3으로 나눈 몫
#print(result5) # 4
#print(type(result5)) # <class 'int'>

# 나머지 연산(%) : 두 숫자를 나누고 나머지를 반환한다.
result6 = 14 % 3 # 14를 3으로 나눈 나머지
#print(result6) # 2

# 거듭제곱(**) : 첫 번째 숫자를 두 번째 숫자만큼 거듭제곱한다.
result7 = 2 ** 3 # 2의 3제곱
#print(result7) # 8

result8 = 10 ** 2 # 10의 2제곱
#print(result8) # 100

# 연산 우선순위 : 수학과 동일하게 괄호 > 거듭제곱 > 곱셈/나눗셈 > 덧셈/뺄셈 순서로 계산된다.
result9 = 2 + 3 * 4 # 곱셈이 먼저 계산된다.
#print(result9) # 14

result10 = (2 + 3) * 4 # 괄호가 먼저 계산된다.
#print(result10) # 20

# ============================================================================
# 숫자형 형변환(Type Conversion)
# ============================================================================
# 형변환 함수 정리표
# ============================================================================
# | 함수      | 설명                    | 예제            | 결과      | 반환 타입 |
# |----------|------------------------|----------------|----------|----------|
# | int()    | 정수형으로 변환          | int(3.14)      | 3        | int      |
# |          |                        | int("123")     | 123      | int      |
# | float()  | 실수형으로 변환          | float(123)     | 123.0    | float    |
# |          |                        | float("3.14")  | 3.14     | float    |
# | str()    | 문자열로 변환            | str(123)       | "123"    | str      |
# |          |                        | str(3.14)      | "3.14"   | str      |
# | complex()| 복소수형으로 변환        | complex(3, 4)  | (3+4j)   | complex  |
# ============================================================================
# 주의: int()로 실수를 변환하면 소수점 이하가 버려집니다.

# int() 함수를 사용하여 다른 타입을 정수형으로 변환할 수 있다.
float_to_int = int(3.14) # 실수를 정수로 변환한다. (소수점 이하 버림)
#print(float_to_int) # 3
#print(type(float_to_int)) # <class 'int'>

str_to_int = int("123") # 문자열을 정수로 변환한다.
#print(str_to_int) # 123
#print(type(str_to_int)) # <class 'int'>

# float() 함수를 사용하여 다른 타입을 실수형으로 변환할 수 있다.
int_to_float = float(123) # 정수를 실수로 변환한다.
#print(int_to_float) # 123.0
#print(type(int_to_float)) # <class 'float'>

str_to_float = float("3.14") # 문자열을 실수로 변환한다.
#print(str_to_float) # 3.14
#print(type(str_to_float)) # <class 'float'>

# str() 함수를 사용하여 숫자를 문자열로 변환할 수 있다.
int_to_str = str(123) # 정수를 문자열로 변환한다.
#print(int_to_str) # "123"
#print(type(int_to_str)) # <class 'str'>

float_to_str = str(3.14) # 실수를 문자열로 변환한다.
#print(float_to_str) # "3.14"
#print(type(float_to_str)) # <class 'str'>

# ============================================================================
# 숫자 관련 내장 함수들
# ============================================================================
# 숫자 관련 내장 함수 정리표
# ============================================================================
# | 함수      | 설명                    | 예제                | 결과      | 반환 타입 |
# |----------|------------------------|---------------------|----------|----------|
# | abs()    | 절댓값 구하기            | abs(-10)            | 10       | int/float|
# | round()  | 반올림                  | round(3.7)          | 4        | int      |
# |          |                        | round(3.14159, 2)   | 3.14     | float    |
# | max()    | 최대값 구하기            | max(1, 5, 3, 9, 2)  | 9        | int/float|
# | min()    | 최소값 구하기            | min(1, 5, 3, 9, 2)  | 1        | int/float|
# | sum()    | 합계 구하기              | sum([1, 2, 3, 4, 5])| 15       | int/float|
# | pow()    | 거듭제곱 구하기          | pow(2, 3)           | 8        | int/float|
# | divmod() | 몫과 나머지 구하기       | divmod(14, 3)       | (4, 2)   | tuple    |
# ============================================================================

# abs() 함수를 사용하여 숫자의 절댓값을 구할 수 있다.
absolute_value = abs(-10)
#print(absolute_value) # 10

absolute_value2 = abs(10)
#print(absolute_value2) # 10

# round() 함수를 사용하여 숫자를 반올림할 수 있다.
rounded1 = round(3.7) # 3.7을 반올림한다.
#print(rounded1) # 4

rounded2 = round(3.2) # 3.2를 반올림한다.
#print(rounded2) # 3

# 소수점 둘째 자리까지 반올림
rounded3 = round(3.14159, 2) # 소수점 둘째 자리까지 반올림한다.
#print(rounded3) # 3.14

# max() 함수를 사용하여 여러 숫자 중 최대값을 구할 수 있다.
maximum = max(1, 5, 3, 9, 2)
#print(maximum) # 9

# 리스트에서도 사용 가능하다.
numbers = [1, 5, 3, 9, 2]
maximum2 = max(numbers)
#print(maximum2) # 9

# min() 함수를 사용하여 여러 숫자 중 최소값을 구할 수 있다.
minimum = min(1, 5, 3, 9, 2)
#print(minimum) # 1

minimum2 = min(numbers)
#print(minimum2) # 1

# sum() 함수를 사용하여 숫자들의 합을 구할 수 있다.
total = sum([1, 2, 3, 4, 5])
#print(total) # 15

# pow() 함수를 사용하여 거듭제곱을 구할 수 있다. (x ** y와 동일)
power = pow(2, 3) # 2의 3제곱
#print(power) # 8

# divmod() 함수를 사용하여 몫과 나머지를 동시에 구할 수 있다.
quotient, remainder = divmod(14, 3) # 14를 3으로 나눈 몫과 나머지
#print(quotient) # 4
#print(remainder) # 2

