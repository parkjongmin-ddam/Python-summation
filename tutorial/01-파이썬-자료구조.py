# ============================================================================
# 주의: 허수부는 j 또는 J를 사용하며, 숫자와 j 사이에 공백이 없어야 합니다.

# 복소수형(Complex) : 실수부와 허수부로 구성된 숫자
complex_num = 3 + 4j # 3은 실수부, 4는 허수부
#print(complex_num) # (3+4j)
#print(type(complex_num)) # <class 'complex'>

# 복소수 생성 함수 사용
complex_num2 = complex(3, 4) # complex(실수부, 허수부)
#print(complex_num2) # (3+4j)

