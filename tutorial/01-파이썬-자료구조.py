# 숫자 비교 연산자
# ============================================================================
# 비교 연산자 정리표
# ============================================================================
# | 연산자 | 의미          | 예제        | 결과  | 설명                    |
# |--------|--------------|------------|------|------------------------|
# | ==     | 같다          | 5 == 5     | True | 두 값이 같은지 확인      |
# |        |              | 5 == 3     | False|                        |
# |--------|--------------|------------|------|------------------------|
# | !=     | 같지 않다      | 5 != 3     | True | 두 값이 다른지 확인      |
# |        |              | 5 != 5     | False|                        |
# |--------|--------------|------------|------|------------------------|
# | >      | 크다          | 5 > 3      | True | 왼쪽이 오른쪽보다 큰지 확인 |
# |        |              | 3 > 5      | False|                        |
# |--------|--------------|------------|------|------------------------|
# | <      | 작다          | 3 < 5      | True | 왼쪽이 오른쪽보다 작은지 확인 |
# |        |              | 5 < 3      | False|                        |
# |--------|--------------|------------|------|------------------------|
# | >=     | 크거나 같다    | 5 >= 5     | True | 왼쪽이 오른쪽보다 크거나 같은지 확인 |
# |        |              | 5 >= 3     | True |                        |
# |        |              | 3 >= 5     | False|                        |
# |--------|--------------|------------|------|------------------------|
# | <=     | 작거나 같다    | 3 <= 5     | True | 왼쪽이 오른쪽보다 작거나 같은지 확인 |
# |        |              | 5 <= 5     | True |                        |
# |        |              | 5 <= 3     | False|                        |
# ============================================================================
# 주의: 비교 연산자는 항상 True 또는 False (불리언 값)를 반환한다.

# == : 같다
result = (5 == 5) # 5와 5가 같은지 확인한다.
#print(result) # True

result2 = (5 == 3) # 5와 3이 같은지 확인한다.
#print(result2) # False

# != : 같지 않다
result = (5 != 3) # 5와 3이 다른지 확인한다.
#print(result) # True

result2 = (5 != 5) # 5와 5가 다른지 확인한다.
#print(result2) # False

# > : 크다
result = (5 > 3) # 5가 3보다 큰지 확인한다.
#print(result) # True

result2 = (3 > 5) # 3이 5보다 큰지 확인한다.
#print(result2) # False

# < : 작다
result = (3 < 5) # 3이 5보다 작은지 확인한다.
#print(result) # True

result2 = (5 < 3) # 5가 3보다 작은지 확인한다.
#print(result2) # False

# >= : 크거나 같다
result = (5 >= 5) # 5가 5보다 크거나 같은지 확인한다.
#print(result) # True

result2 = (5 >= 3) # 5가 3보다 크거나 같은지 확인한다.
#print(result2) # True

result3 = (3 >= 5) # 3이 5보다 크거나 같은지 확인한다.
#print(result3) # False

# <= : 작거나 같다
result = (3 <= 5) # 3이 5보다 작거나 같은지 확인한다.
#print(result) # True

result2 = (5 <= 5) # 5가 5보다 작거나 같은지 확인한다.
#print(result2) # True

result3 = (5 <= 3) # 5가 3보다 작거나 같은지 확인한다.
#print(result3) # False

# ============================================================================
# 숫자와 문자열의 연산
# ============================================================================
# 숫자와 문자열 연산 규칙
# ============================================================================
# | 연산          | 결과              | 설명                    |
# |--------------|------------------|------------------------|
# | 숫자 + 숫자   | 숫자 (정상)       | 산술 연산 수행          |
# | 문자열 + 문자열| 문자열 (정상)   | 문자열 연결             |
# | 숫자 + 문자열 | TypeError 발생    | 타입이 다르므로 불가능   |
# | 숫자 + int(문자열) | 숫자 (정상) | 형변환 후 연산 가능      |
# ============================================================================
# 주의: 숫자와 문자열을 직접 연산하려고 하면 TypeError가 발생합니다.
# 해결 방법: 형변환을 통해 같은 타입으로 만든 후 연산합니다.

# 숫자와 문자열은 직접 연산할 수 없다. (에러 발생)
# num = 10
# text = "20"
# result = num + text # TypeError 발생!

# 형변환을 통해 연산할 수 있다.
num = 10
text = "20"
result = num + int(text) # 문자열을 정수로 변환한 후 연산한다.
#print(result) # 30