# 복소수형(Complex) : 실수부와 허수부로 구성된 숫자
# ============================================================================
# 복소수 표현 방법 정리표
# ============================================================================
# | 방법            | 예제          | 결과      | 설명                    |
# |----------------|--------------|----------|------------------------|
# | 직접 표현       | 3 + 4j       | (3+4j)   | 실수부 + 허수부j        |
