# 숫자 타입 확인하기
# ============================================================================
# 타입 확인 함수 정리표
# ============================================================================
# | 함수          | 설명                    | 예제                  | 결과      | 반환 타입 |
# |--------------|------------------------|----------------------|----------|----------|
