# 조건문 (if, elif, else)
# 조건문은 프로그램의 흐름을 제어하는 데 사용된다.
# 특정 조건이 참(True)일 때만 코드 블록을 실행하도록 한다.

# ============================================================================
# 기본 if 문
# ============================================================================
# if 조건식:
#     실행할 코드

age = 20

if age >= 18:
    #print("성인입니다.") # age가 18 이상이면 실행
    pass

# ============================================================================
# if-else 문
# ============================================================================
# if 조건식:
#     실행할 코드 (조건이 True일 때)
# else:
#     실행할 코드 (조건이 False일 때)

age = 15

if age >= 18:
    #print("성인입니다.")
    pass
else:
    #print("미성년자입니다.") # age가 18 미만이면 실행
    pass

# ============================================================================
# if-elif-else 문
# ============================================================================
# 여러 조건을 순차적으로 확인할 때 사용한다.

score = 85

if score >= 90:
    #print("A등급")
    grade = "A"
elif score >= 80:
    #print("B등급") # score가 80 이상 90 미만이면 실행
    grade = "B"
elif score >= 70:
    #print("C등급")
    grade = "C"
else:
    #print("F등급")
    grade = "F"

# ============================================================================
# 중첩 if 문
# ============================================================================
# if 문 안에 또 다른 if 문을 사용할 수 있다.

age = 25
has_license = True

if age >= 18:
    if has_license:
        #print("운전 가능합니다.")
        pass
    else:
        #print("면허가 필요합니다.")
        pass
else:
    #print("미성년자는 운전할 수 없습니다.")
    pass

# ============================================================================
# 논리 연산자와 함께 사용
# ============================================================================

age = 25
has_license = True

# and 연산자: 모든 조건이 True여야 True
if age >= 18 and has_license:
    #print("운전 가능합니다.")
    pass

# or 연산자: 하나 이상의 조건이 True이면 True
if age < 18 or not has_license:
    #print("운전할 수 없습니다.")
    pass

# not 연산자: 조건을 반전
if not has_license:
    #print("면허가 없습니다.")
    pass

# ============================================================================
# 비교 연산자
# ============================================================================
# | 연산자 | 설명          | 예제        |
# |--------|--------------|------------|
# | ==     | 같음          | a == b     |
# | !=     | 다름          | a != b     |
# | <      | 작음          | a < b      |
# | >      | 큼            | a > b      |
# | <=     | 작거나 같음    | a <= b     |
# | >=     | 크거나 같음    | a >= b     |
# | is     | 동일 객체      | a is b     |
# | is not | 다른 객체      | a is not b |
# | in     | 포함          | a in b     |
# | not in | 미포함        | a not in b |

x = 10
y = 20

if x == y:
    #print("x와 y가 같습니다.")
    pass

if x != y:
    #print("x와 y가 다릅니다.")
    pass

if x < y:
    #print("x가 y보다 작습니다.")
    pass

# ============================================================================
# 삼항 연산자 (조건부 표현식)
# ============================================================================
# 간단한 조건문을 한 줄로 작성할 수 있다.

age = 20
status = "성인" if age >= 18 else "미성년자"
#print(status) # "성인"

# 위 코드는 아래와 동일하다:
if age >= 18:
    status = "성인"
else:
    status = "미성년자"

# ============================================================================
# 여러 조건을 한 줄에 작성
# ============================================================================

x = 10
y = 20
z = 30

# 여러 조건을 and로 연결
if x < y and y < z:
    #print("x < y < z")
    pass

# 여러 조건을 or로 연결
if x == 10 or y == 20:
    #print("x는 10이거나 y는 20입니다.")
    pass

# ============================================================================
# in 연산자를 사용한 멤버십 테스트
# ============================================================================

fruits = ["apple", "banana", "cherry"]

if "apple" in fruits:
    #print("사과가 있습니다.")
    pass

if "orange" not in fruits:
    #print("오렌지가 없습니다.")
    pass

# 문자열에서도 사용 가능
text = "Hello, World!"

if "Hello" in text:
    #print("'Hello'가 포함되어 있습니다.")
    pass

# ============================================================================
# is와 ==의 차이
# ============================================================================
# == : 값이 같은지 비교
# is : 객체가 동일한지 비교 (메모리 주소)

a = [1, 2, 3]
b = [1, 2, 3]
c = a

if a == b:
    #print("a와 b의 값이 같습니다.") # True
    pass

if a is b:
    #print("a와 b는 같은 객체입니다.") # False (다른 객체)
    pass

if a is c:
    #print("a와 c는 같은 객체입니다.") # True (같은 객체)
    pass

# None 비교 시에는 is를 사용하는 것이 권장됨
value = None

if value is None:
    #print("값이 None입니다.")
    pass

# ============================================================================
# pass 문
# ============================================================================
# 아무것도 실행하지 않지만 문법적으로 코드 블록이 필요할 때 사용한다.

if True:
    pass # 아무것도 하지 않음

